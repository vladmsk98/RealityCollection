# RealityCollection

**RealityCollection** — это интерактивная веб-страница, посвящённая нейросетевому музыкальному проекту. Она представляет собой коллекцию статей с описанием треков, сопровождающихся аудио-файлами. Страница адаптирована под разные устройства и включает функцию раскрывающихся статей для удобного просмотра.

## Особенности

*   **Интерактивные статьи:** Заголовки статей можно кликать для открытия и закрытия содержимого.
*   **Аудио-плееры:** Встроенные HTML5 аудио-плееры для прослушивания треков.
*   **Адаптивный дизайн:** Страница корректно отображается на различных устройствах (десктопы, планшеты, мобильные).
*   **Чистая стилизация:** Современные и чистые стили, обеспечивающие хорошее визуальное восприятие.

## Технологии

*   **HTML5:** Для структуры веб-страницы.
*   **CSS3:** Для стилизации и адаптивности.
*   **JavaScript (ES5):** Для интерактивности (переключение статей).

## Структура проекта

```
.
├── index.html          # Основной HTML-файл
├── style.css           # Файл стилей
├── script.js           # Файл скриптов
├── image/              # Папка с изображениями (например, BANNER.png)
└── audio/              # Папка с аудио-файлами
```

